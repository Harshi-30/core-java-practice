class Variables{
    public static void main(String[] args){
        byte a = 1;
        short b = 2;
        int c = 4;
        long d = 8l;
        char e = '1';
        float f = 4.0f;
        double g = 8.0;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d) ;
        System.out.println("e = " + e) ;
        System.out.println("f = " + f) ;
        System.out.println("g = " + g) ;


    }
}